package com.project.group17.group.entity;

/**
 * Represents a POJO (Plain Old Java Object) for holding group detail information.
 */
public class GroupDetailPojo {
    Long groupId;

    /**
     * Gets the group ID.
     *
     * @return The group ID.
     */
    public Long getGroupId() {
        return groupId;
    }

    /**
     * Sets the group ID.
     *
     * @param groupId The group ID to set.
     */
    public void setGroupId(Long groupId) {
        this.groupId = groupId;
    }
}
